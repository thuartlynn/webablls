            <div>
                <h5>Profile Coordinator | Transfer to another User Account</h5>
                <p class="ml-4">As with many functions within WebABLLS 2.0, there are several processes that overlap with various ways to accomplish the same result. The admin function of transferring a student is one of these. Either procedure you choose will work effectively – it is personal choice as to which method works best for you.<p>

                <h5>Admin User Instructions – Transfer Profile Coordinator method 1</h5>

                <p class="ml-4">Click the <strong>Organization</strong> tab/link from the top banner <p>
                <p class="ml-4">Click <strong>Organization User</strong> List link from the action panel<p>
                <p class="ml-4"><strong>Click on the row for the corresponding user name to highlight (select)</strong><p>
                <p class="ml-4">Click the <strong>Related Students</strong> link from the action panel<p>

                <p class="ml-4"><strong>To transfer ALL associated students</strong><p>

                <ul>
                    <li>Click <strong>Select Replacement for ALL</strong></li>
                    <li>The replacement for User screen will appear</li>
                    <li>Use the drop-down list to <strong>select an alternative Profile Coordinator</strong></li>
                    <li>Click the <strong>Transfer rights</strong> link to complete the process</li>
                </ul>

                <p class="ml-4"><strong>To transfer only a particular student</strong><p>

                <ul>
                    <li><strong>Click on the row for the corresponding student to highlight (select)</strong></li>
                    <li>Click the <strong>Select Replacement</strong> link</li>
                    <li>The replacement for User screen will appear</li>
                    <li>Use the drop-down list to <strong>select an alternative Profile Coordinator</strong></li>
                    <li>Click the <strong>Transfer rights</strong> link to complete the process</li>
                </ul>

                <h5>Admin User Instructions – Transfer Profile Coordinator method 2</h5>

                <p class="ml-4">Click the <strong>Organization</strong> tab/link from the top banner <p>

                <p class="ml-4">Click <strong>Student Roster</strong> link from the action panel<p>

                <p class="ml-4"><strong>Click on the row for the corresponding student to highlight (select)</strong><p>

                <p class="ml-4">Click the <strong>Edit Profile</strong> link from the action panel<p>

                <p class="ml-4">The Student Profile (Admin) screen will appear<p>
                <ul>
                    <li>Scroll to the bottom of the profile screen and locate the <strong>Profile Coordinator</strong> field</li>
                    <li>Use the drop-down list to <strong>select an alternative Profile Coordinator</strong></li>
                </ul>

                <p class="ml-4">Be sure to <strong>SAVE</strong> your changes<p>

                <h5>Standard User Instructions – must be the current profile coordinator</h5>

                <p class="ml-4">Locate the Student Profile screen from the Dashboard or Student List<p>
                <ul>
                    <li><strong>Dashboard</strong> | Click the <strong>student ID#</strong> from Recent History</li>
                    <li>Student List | Click <strong>Students</strong> tab from top banner, locate and click <strong>student ID#</strong> from list</li>
                </ul>

                <p class="ml-4">The Student Profile screen will appear<p>
                <ul>
                    <li>Scroll to the bottom of the profile screen and locate the <strong>Profile Coordinator</strong> field</li>
                    <li>Use the drop-down list to <strong>select an alternative Profile Coordinator</strong></li>
                </ul>

                <p class="ml-4">Be sure to <strong>SAVE</strong> your changes <p>
