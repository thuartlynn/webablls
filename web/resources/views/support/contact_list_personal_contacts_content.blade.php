                <p class="ml-4">Contacts are used for authorizing sharing permissions, or linking, a student profile with another WebABLLS user. WebABLLS student profiles can be shared with any registered user account within the WebABLLS system. Your Personal Contacts list is where you can add contacts that are not within your organization to authorize permissions.<p>
                <p class="ml-4"><strong style="color: #B22222;">Important!</strong> This procedure will add an established user account to your Contact List for the purpose of sharing a student profile with another user account.<u style="color: #B22222;">This procedure does not create a user account</u>.<p>
                <p class="ml-4"><strong>To add a Personal Contact from the Contacts List screen (recommended)</strong><p>
                    <ul>
                        <li>Click the <strong>Contacts</strong> tab from the navigation links located in the top banner</li>
                        <li>Click <strong>Add Contact</strong> link from the Contacts action panel</li>
                        <li>Enter the Contact’s name and email address</li>
                        <li>Click the <strong>Add Contact</strong> link</li>
                        <li>The screen will refresh and the Contact will display in the Personal Contacts section</li>
                        <li>If the <strong>“Is User”</strong> column is marked <strong>Yes</strong>, the email address is registered in the system – you can proceed with authorizing sharing permissions between a student and this user</li>
                        <li>If the <strong>“Is User”</strong> column is marked <strong>No</strong>, the email address is not associated with a current user account and a user account will need to be created by an administrator user from within your organization OR by the WebABLLS Support Team</li>
                    </ul>
                <p class="ml-4"><strong>To add a Personal Contact from the Student Share screen</strong><p>
                    <ul>
                        <li>Locate the <strong>Student Summary</strong> screen from the Dashboard or Student List</li>
                        <li><strong>Dashboard</strong> | Click the student name from Recent History</li>
                        <li>Student List | Click <strong>Students</strong> tab from top banner, locate and click student name from list (use filters if necessary to locate student quickly)</li>
                        <li>The <strong>Student Summary</strong> screen will appear</li>
                        <li>Locate and click the <strong>Share</strong> link from the list of links in the <strong>Student action panel</strong></li>
                        <li>The <strong>Share Student</strong> screen will appear</li>
                        <li>Locate the <strong>Add Contact</strong></li>
                        <li>Enter the Contact’s email address</li>
                        <li>Click the <strong>Add Contact</strong> link</li>
                        <li>If the email address is registered to a user account within WebABLLS, the system will display the following message: <I>“Contact added. <span class="sup-a" style="color: #254689; overflow:hidden; white-space: nowrap; text-overflow: ellipsis;">Reload page</span> to see it on list of contacts.”</I> Reload the page and the contact will appear in your Personal Contacts list.</li>
                        <li>If the email address is NOT registered to a user account within WebABLLS, the system will display the following message: <I>“Contact added. This person does not have an account in WebABLLS</I>. You cannot share a student with this person.”  The email address is not associated with a current user account and a user account will need to be created by an administrator user from within your organization OR by the WebABLLS Support Team.</li>
                    </ul>
                <p class="ml-4"><strong>TIP</strong> | For organizations with many users in your Organization Members list, you can add frequently-used contacts to your Personal Contacts to locate them quickly when authorizing sharing permissions.<p>
