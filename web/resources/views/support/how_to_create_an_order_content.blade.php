            <hr class="hr">
            <div>
                <h5>How to Create an Order</h5>

                <p class="ml-4"><strong>Admin user instructions</strong><p>

                <p class="ml-4"><strong>Open Order</strong> link is available from the <strong>Dashboard</strong> screen or within the <strong>Organization</strong> tab<p>

                <p class="ml-4"><strong>Important!</strong><p>
                <p class="ml-4"><strong>All orders are now created in WebABLLS</strong><p>
                <ul>
                    <li><strong>Pay by Credit Card for immediate activation</strong></li>
                    <li><strong>Use Pay without Credit Card feature for Purchase Orders or other forms of payment – See Step 6 for details!</strong></li>
                </ul>

                <p class="ml-4"><strong>Key Steps</strong><p>
                <ol>
                    <li><strong>Open Order link</strong></li>
                    <li><strong>Create Extension Order</strong></li>
                    <ul>
                        <li><strong>Enter order parameters</strong></li>
                        <li><strong>Number of years or <span style="color: #B22222;">Add seats to existing subscription</span></strong></li>
                        <li><strong>Extended Seats</strong> (select seats to renew)</li>
                        <li><strong>Additional Seats</strong></li>
                        <li><strong>When to activate</strong> (now or later)</li>
                    </ul>
                    <li><strong>Billing Address</strong></li>
                    <li><strong>Process Order (checkout)</strong></li>
                    <li><strong>Print order documents</strong></li>
                    <li><strong>Submit Order documents + payment to the Order Department</strong></li>
                </ol>

                <p class="ml-4"><strong>Tips</strong><p>
                <ul>
                    <li><strong>Use links to toggle back and forth through the order portal</strong></li>
                    <li><strong>Use the Cancel link to start over</strong></li>
                    <li><strong>Standard pricing for an additional seat is $100 = $40 activation + $60 for a full 12-month extension</strong></li>
                    <li><strong>Price calculations for additional seats will be pro-rated @ $5 per month to the Organization’s expiration date</strong></li>
                    <li><strong>Additional seats will display the standard activation fee of $40 + the extension per month</strong></li>
                </ul>

                <p class="ml-4"><strong>Step 1 | Create a new order</strong><p>
                <ul>
                    <li>Click the <strong>Open Order</strong> link</li>
                    <li>Click the <strong>Select Extension Options</strong> link</li>
                    <li><strong>Create Extension Order</strong> screen will display</li>
                    <li>Select order options</li>
                    <ul>
                        <li><strong>Subscription Extension</strong> – select number of years for renewal <u>or</u> <span style="color: #B22222;">select <strong>Add seats to existing subscription</strong> if purchasing only additional seats</span></li>
                        <li><strong>Extended Seats</strong> – the 3 choices within the section relate to renewal extensions</li>
                        <li><strong>Additional Seats</strong> – enter the number of additional seats</li>
                        <li><strong>Activate Now</strong> – activates seats upon completion of the order; cost is pro-rated to the annual expiration date</li>
                        <li><strong>Delayed activation</strong> – seats are purchased but are not available until the annual activation date</li>
                    </ul>
                    <li>Click <strong>Confirm</strong></li>
                </ul>

                <p class="ml-4"><strong>Step 2 | Review and confirm order</strong><p>
                <ul>
                    <li>The <strong>Order Summary</strong> screen will display</li>
                    <li>Review and verify the Order Summary for accuracy</li>
                    <ul>
                        <li>Verify the total amount</li>
                        <li>Verify the expiration date</li>
                        <li>Verify the total seat count for your organization</li>
                    </ul>
                    <li>If you need to make a change, click <strong>Select Extension Options</strong> to return to the previous screen</li>
                    <li>When order is correct, click <strong>Confirm Order</strong></li>
                    <li>The Billing Address screen will display</li>
                </ul>

                <p class="ml-4"><strong>Step 3 | Billing Address & Checkout</strong><p>
                <ul>
                    <li>Verify billing address</li>
                    <ul>
                        <li>Follow steps to enter <strong>Billing Address</strong> and click <strong>Add New Address</strong></li>
                        <li>Or locate and click the <strong>Use this Address</strong> link if the correct address is available</li>
                    </ul>
                </ul>

                <p class="ml-4"><strong>CREDIT CARD Purchases</strong><p>

                <p class="ml-4"><strong>Step 4 | Process Order (checkout)</strong><p>
                <ul>
                    <li>The <strong>Process order</strong> screen will display</li>
                    <li>For credit card orders</li>
                    <ul>
                        <li>Enter payment details</li>
                        <li>Click Confirm Payment</li>
                    </ul>
                    <li>The screen will refresh and display a thank you message – and your order will be activated upon payment confirmation.</li>
                </ul>

                <p class="ml-4"><strong>Step 5 | Print order documents</strong><p>
                <ul>
                    <li>Click the <strong>Orders</strong> link from the action panel – the last link in the list</li>
                    <li>Your order will appear in the table</li>
                    <li>Click on the row that corresponds with the order (it will highlight in yellow)</li>
                    <li>Click the <strong>Download Summary</strong> and <strong>Download Invoice</strong> links to generate a PDF of the files</li>
                </ul>
                <p class="ml-4"><strong>Note</strong> | It may take a few minutes for the Invoice file to be generated and appear in WebABLLS<p>

                <p class="ml-4"><strong>PURCHASE ORDERS</strong> (or other forms of payment)<p>

                <p class="ml-4"><strong>Step 4 | Process Order (checkout)</strong><p>
                <ul>
                    <li>The <strong>Process order</strong> screen will display</li>
                    <li>Scroll down to the <strong>Other Payment Methods</strong> section</li>
                    <li><u>Read the instructions</u></li>
                    <li>Click <strong>Confirm Order without Credit Card</strong></li>
                    <li>The screen will refresh and display a thank you message – and your order details are stored within your WebABLLS organization</li>
                </ul>

                <p class="ml-4"><strong>Step 5 | Print order documents</strong><p>
                <ul>
                    <li>Click the <strong>Orders</strong> link from the action panel – the last link in the list</li>
                    <li>Your order will appear with a status of <strong>Payment pending/Waiting for Confirmation</strong></li>
                    <li>Click on the row that corresponds with the order (it will highlight in yellow)</li>
                    <li>Click the <strong>Download Summary</strong> and Download Invoice links to generate a PDF of the files</li>
                </ul>
                <p class="ml-4"><strong>Note</strong> | It may take a few minutes for the Invoice file to be generated and appear in WebABLLS<p>

                <p class="ml-4"><strong>Step 6 | Submit Order documents with order</strong><p>
                <p class="ml-4"><strong><span style="color: #B22222;">IMPORTANT! DO NOT SKIP THIS STEP</span></strong><p>
                <ul>
                    <li>Print both the <u>Order Summary</u> and the preliminary <u>invoice</u> and submit to the Order Department with your purchase order or other payment method</li>
                    <li>Upon receipt  and verification of order documents and purchase order or payment by the Order Department,  the WebABLLS Support Team will confirm the activation of the order</li>
                </ul>
            </div>
