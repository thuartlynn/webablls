                <p class="ml-4">To edit any details related to an assessment (date, color, current program/service information); simply click on Assessment Details from any assessment edit screen or the Details link for the corresponding assessment from the Student Summary screen.</p>

                <p class="ml-4"><strong>NOTE:</strong> Assessment edit modes are simply a matter of personal choice. There are no features or functions that are different between the three display types. Grid Mode resembles the skills tracking grids of the protocol; Text Mode resembles the print version of the ABLLS-R Protocol; and Cat Mode (short for category) is another format offered to accommodate scoring from a smaller screen, tablet, or smart phone by displaying individual categories of the assessment in the grid style.</p>

                <p class="ml-4"><strong>Assessment (Assigned) date:</strong> The system will provide an automatic date/time stamp upon creation; however, the user can define an alternative date to correspond with the actual date of assessment. The date entered in this field will display on the legend associated with the assessment.</p>

                <p class="ml-4"><strong>Grade Level:</strong> Use the drop-down list to select the corresponding grade level (this field is not required).</p>

                <p class="ml-4"><strong>Complete-Zero Scoring:</strong> Applies to only the initial assessment. An important attribute on this page is the checkbox that activates all assessment fields to be scored as zero by filling the dot in the circle to the left of each task on the tracking grid. This feature is available for an initial assessment only. This is recommended for students who are considered early learners (very few initial skills). The default is set for Complete-Zero Scoring to occur for all initial assessments. WebABLLS will automatically mark each of the over 500 skills with a score of zero, eliminating the need for each of the skills to be marked manually. During the initial assessment period as scoring data is entered for the skills that do meet any level of the task criteria, the color in the dot will be removed automatically. To remove this default setting, simply uncheck the box.</p>

                <p class="ml-4"><strong>Is sign language a potential response form?:</strong>  This setting corresponds with the Student Profile setting but can be revised here if necessary.​</p>

                <p class="ml-4"><strong>Assessment Color:</strong> Select a color from any of the color charts available. Each assessment must be assigned a unique color – WebABLLS will not allow an assessment color to be duplicated on a subsequent assessment for the same student. [Note: The assessment color can be revised after creation from the Assessment Details screen.]</p>

                <p class="ml-4"><strong>Program Level during the time of assessment:</strong> Optional, but recommend; use the fields to document the student’s current services and programs.</p>

                <p class="ml-4"><strong>Assessment Edit Method:</strong> This field is set to match the scoring/edit method chosen in the user preferences (Account screen); however, any of the three methods can be chosen from this screen or from any assessment edit screen.</p>

