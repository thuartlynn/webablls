                <h4>What does the Permissions column in the Student List mean?</h4>
                <p class="ml-4"><small>Last Updated: Jun 21, 2019 06:16AM PDT</small></p>
            </div>
            <hr class="hr">
            <div>
                <p class="ml-4">The permissions column shows you the access level you have for each student in your student list. If you are the owner of a student then you will have full access, but if someone shares a student with you they could grant you full access, but they could also limit your access level.</p>

                <p class="ml-4">The available access levels are the following:</p>
                <p class="ml-4"><strong>Owner</strong> - All Access</p>
                <p class="ml-4"><strong>Profile Coordinator</strong> - All Access</p>
                <p class="ml-4"><strong>FA</strong> - Full Access- FA- None</p>
                <p class="ml-4"><strong>Basic Info</strong> - None - View - Modify</p>
                <p class="ml-4"><strong>Assessments and Reports</strong> - None - View - Modify</p>
                <p class="ml-4"><strong>Files</strong> - None - View - Modify</p>
                <p class="ml-4"><strong>Analytics</strong> - None - View - Modify</p>

                <p class="ml-4">Share permissions can be authorized from the <strong>Student List</strong> screen by highlighting a student and selecting the <strong>Share Student</strong> link from the action panel or by clicking the <strong>Share</strong> link from the <strong>Student Summary</strong> screen. </p>

                <p class="ml-4"><strong>Note</strong> | User must have Owner, Profile Coordinator, or Full Access with Share permissions to authorize share permissions </p>

                <hr class="hr">

                <p class="ml-4"><strong>Share Permissions acronyms:</strong></p>
                <p class="ml-4"><strong>O</strong>     Owner rights</p>
                <p class="ml-4"><strong>CO</strong>   Profile Coordinator</p>
                <p class="ml-4"><strong>FAs</strong>  Full access with authorization to manage sharing rights</p>
                <p class="ml-4"><strong>FA</strong>    Full access</p>
                <p class="ml-4"><strong>Iv</strong>    Basic INFO view [Student Profile screen]</p>
                <p class="ml-4"><strong>Im</strong>   Basic INFO modify [Student Profile screen]</p>
                <p class="ml-4"><strong>Av</strong>   Assessments and Reports view</p>
                <p class="ml-4"><strong>Am</strong>  Assessments and Reports modify</p>
                <p class="ml-4"><strong>Fv</strong>   Files view</p>
                <p class="ml-4"><strong>Fm</strong>  Files modify</p>
                <p class="ml-4"><strong>DAv</strong> Analytics view</p>
                <p class="ml-4"><strong>DAm</strong> Analytics modify</p>

                <p class="ml-4">To remove share permissions, click <strong>Remove Own Permissions</strong> link located in the action panel on the <strong>Student Summary</strong> screen.</p>
