                <h4>How to Remove a Report</h4>
                <p class="ml-4"><small>Last Updated: Mar 09, 2017 07:12PM PST</small></p>
            </div>
            <hr class="hr">
            <div>
                <p class="ml-4"><strong>From the REPORTS screen:</strong><p>
                <ol>
                    <li>Click the <strong>Reports</strong> link from the navigation links in the top banner (the <strong>All Reports</strong> screen will display)</li>
                    <li>Use any or all of the filters or column sort to locate the target report.</li>
                    <ul>
                        <li>A. Use the <strong>Report Type Filter</strong> to locate the Report type (select Program Worksheet, Progress Report, Status Report, or Baseline Report)</li>
                        <li>B. Use the <strong>Students Filter</strong> to isolate the reports related to a particular student (enter student’s first, last name, or ID#)</li>
                        <li>C. Use the Assessment Color Filter to isolate reports related to a particular assessment period</li>
                    </ul>
                    <li>Select the report by clicking on the row that corresponds to highlight the report you wish to remove</li>
                    <li>Locate and click the <strong>Remove Report</strong> link within the Actions panel</li>
                </ol>

                <img class="ml-4" src="{{ URL::asset('images/support/how-to-remove-a-report-1.png') }}">

                <br>
                <br>

                <p class="ml-4"><strong>From the STUDENT SUMMARY screen:</strong><p>

                <ol>
                    <li>Click the <strong>Student’s Name</strong> from the Recent History panel (or from any screen with your student list displayed)</li>
                    <li>The <u>Student Summary</u> screen will display</li>
                    <li>Click the <strong>Reports</strong> link from the Student action panel</li>
                    <li>Use the <strong>Report Type Filter</strong> (left sidebar action panel) and /or column sort to locate the Report</li>
                    <li>Select the report by clicking on the row that corresponds to highlight the report you wish to remove</li>
                    <li>Locate and click the <strong>Remove Report</strong> link within the Actions panel.</li>
                </ol>
