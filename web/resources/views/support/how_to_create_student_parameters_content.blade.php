            <div>
                <h4>How to Create Student Parameters</h4>
                <p class="ml-4"><small>Last Updated: Jan 19, 2017 04:39PM PST</small></p>
            </div>
            <hr class="hr">
            <div>
                <p class="ml-4"><strong>Creating Student Parameters</strong><p>
                <p class="ml-4"><strong>Who can perform this function?</strong><p>
                <p class="ml-4"><strong>Admin users | Yes</strong><p>
                <p style="color: gray;" class="ml-4">Standard Users | No<p>
                <p style="color: gray;" class="ml-4">Restricted Users | No<p>

                <p class="ml-4">Student parameters provide a mechanism for organizations to define unique or custom markers that will appear in a ‘location’ field within the Student Profile section. Parameters can be school sites, agency locations, classrooms, or any other type of segment or section that defines a group or cluster of students.  For example, if a parameter is set for ABC Classroom, then within the Student List filters, the user can select the ABC Classroom to see only those specific students.<p>

                <p class="ml-4">There are a total of three sets of parameter filters that can be created. To set your organization parameters simply complete the form by adding a Parameter Name (e.g., Classroom), along with the associated values (ABC Classroom, XYZ Classroom, etc.). Use the Remove link to delete a parameter or the Add link for more values to be added. Continue to the second and third tier, if desired. Save your entries by clicking the Save link at the bottom of the screen.<p>

                <p class="ml-4">Once a parameter field is created, the Student Profile screen for all students within the organization will display the parameter field on the Student Profile screen with a drop-down list that includes all of the values entered.<p>

                <p class="ml-4"><strong>Terms:</strong><p>
                <p class="ml-4"><strong>Parameter name</strong> = the title assigned to a particular parameter set<p>
                <p class="ml-4"><strong>Value</strong> = the corresponding segments related to a parameter set<p>

                <p class="ml-4"><span style="color: #B22222;"><strong>Recommendation:<strong></span> Prior to adding parameters determine the various sets of parameters and corresponding values that reflect the types of student profile groupings that relate with your organization’s needs. All parameters and values can be edited; however, users report planning your sets and values ahead of time is helpful.<p>

                <p class="ml-4"><strong>Locate the Organization Parameters feature</strong><p>
                <ul>
                    <li>Click the <strong>Organization</strong> tab from the navigation links located in the top banner</li>
                    <li>Click <strong>Student Parameters</strong> link from the action panel</li>
                    <li>The <strong>Organization Parameters</strong> screen will appear</li>
                </ul>

                <img class="ml-4" src="{{ URL::asset('images/support/how-to-create-student-parameters.jpg') }}">

                <ol>
                    <li>Check box to activate parameter</li>
                    <li>Enter parameter set name</li>
                    <li>If parameter is a required field, check box [Note: It is recommended that at least one parameter set be required.]</li>
                    <li>Enter values that correspond with the parameter name (click <strong>Remove</strong> button if entering fewer than 3 value fields)</li>
                    <li>Click the <strong>Add</strong> button to open additional value slots</li>
                    <li><strong>Save</strong></li>
                </ol>

                <p class="ml-4">The above actions can be repeated for up to three (3) sets of parameter fields with an unlimited number of values for each set.<p>

                <p class="ml-4"><strong>To verify and/or set parameters (admin users)</strong><p>
                <ul>
                    <li>Click <strong>Student Roster</strong> from the action panel</li>
                    <li>Click on the <strong>ID#</strong> of any student to access the Student Profile screen</li>
                    <li>The parameter field(s) will display just below the field marked, “City”</li>
                    <li>If parameter is set as required, the field will display an asterisk</li>
                    <li>Select a value from the drop-down list</li>
                    <li><strong>Save Changes</strong></li>
                </ul>

                <p class="ml-4"><strong>To set parameters (all users)</strong><p>
                <ul>
                    <li>Locate student from the <strong>Dashboard</strong> or <strong>Students</strong></li>
                    <li>Click on the <strong>ID#</strong> of the student to access the Student Profile screen</li>
                    <li>The parameter field(s) will display just below the field marked “City”</li>
                    <li>If parameter is set as required, the field will display an asterisk</li>
                    <li>Select a value from the drop-down list</li>
                    <li><strong>Save Changes</strong></li>
                </ul>
