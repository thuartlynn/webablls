            <div>
                <p class="ml-4"><strong>Creating Reports</strong><p>
                <p class="ml-4">The <strong>Total Grid View</strong> screen is the hub for creating a variety of reports within WebABLLS, The Total Grid View link is available in the action panel on the Student List and Student Summary screens (and other screens that relate to a student).<p>

                <ul>
                    <li>Program Worksheet</li>
                    <li>Progress Report</li>
                    <li>Status Report</li>
                    <li>Baseline Report</li>
                    <li>Completed Items Report</li>
                    <li>Incomplete Items Report</li>
                </ul>

                <img class="ml-4" src="{{ URL::asset('images/support/reports-in-webablls.jpg') }}">

                <p class="ml-4"><strong>Program Worksheet</strong><p>
                <p class="ml-4">A Program Worksheet is a custom document that includes detailed information regarding tasks selected as target objectives to include in a student’s program plan. The objective text can be modified to reflect a specific need, the current level of performance can be entered, and there is a field to add custom notes.<p>

                <p class="ml-4">To create a Program Worksheet select the skills or tasks to be included in the worksheet by highlighting the tasks from the grid on the Total Grid View screen.  WebABLLS allows up to 50 tasks for selection for any one Program Worksheet – there is a Limit counter located just below the Actions panel on the Total Grid View screen.<p>

                <p class="ml-4">Once the desired tasks are selected, click the Generate Program Worksheet link from the Actions panel to proceed.<p>

                <p class="ml-4">Program Worksheet Screen<p>
                <ul>
                    <li>The selected tasks will appear in a worksheet </li>
                    <li>Select a report title from drop-down list and enter assigned date (see quick note)</li>
                </ul>

                <p class="ml-4">The components of the <strong>Program Worksheet</strong> include five columns—<strong>Skills</strong>, <strong>Objective</strong>, <strong>Criteria</strong>, <strong>Current Level</strong>, and Program Notes. The following three columns, Objective, Current Level, and Program Notes will display text boxes for modifying and/or entering text. To enter text, simply click in the corresponding text box and type in your information. It is recommended you save your data periodically during this process. Should you need to add or remove any tasks included in the worksheet, use the Reselect Items link from the Actions panel to return to the Total Grid View screen to revise your selections (click Save Changes to return to the worksheet).<p>

                <p class="ml-4">To return to the <strong>Program Worksheet</strong> to modify entries or continue, the document will display on the <strong>Student Summary</strong> screen (as well as in the Reports section). Click the <strong>Edit</strong> link next to the corresponding report displayed on the <strong>Student Summary</strong> screen or locate the worksheet in the Reports display (using the filters will be helpful), highlight the student row and click the <strong>Edit Report</strong> link located in the Actions panel.<p>

                <p class="ml-4"><strong>Program Worksheets</strong>, with or without notes, can be downloaded as a PDF or CSV file – click the preferred download from the Actions panel from the <strong>Program Worksheet</strong> screen.<p>

                <p class="ml-4"><strong>Progress Report</strong><p>
                <p class="ml-4">A Progress Report corresponds with a specific Program Worksheet and provides a simple way to update benchmark milestones with regard to skill acquisition. To create a Progress Report, click the Generate Progress Report link from the Actions panel (while on the Program Worksheet screen).  WebABLLS will display the corresponding Progress Report with the original “Current Level” text transferred to the “Previous Level” column – so the new Current Level of performance information can be entered.  A corresponding Progress Report can be created as many times as necessary – weekly, bi-weekly, monthly, etc.  <p>

                <p class="ml-4">Progress Reports, with or without notes, can be downloaded as a PDF or CSV file – click the preferred download from the Actions panel from the Progress Report screen.<p>

                <p class="ml-4"><strong>Status Report</strong><p>
                <p class="ml-4">A <strong>Status Report</strong> is a document that displays a quick reference of the current assessment scoring status with regard to a specific group of selected tasks. As with the Program Worksheet, you will need to start on the <strong>Total Grid View</strong> screen.<p>

                <p class="ml-4">To create a <strong>Status Report</strong> select the skills or tasks to be included in the report by highlighting tasks from the <strong>Total Grid View</strong> screen.  WebABLLS allows up to 50 tasks for selection for any one Status Report – there is a Limit counter located just below the Actions panel on the Total Grid View screen.<p>

                <p class="ml-4">Once the desired tasks are selected, click the <strong>Generate Status Report</strong> link from the Actions panel to create the report.<p>


                <p class="ml-4"><strong>Baseline Report</strong><p>
                <p class="ml-4">A <strong>Baseline Report</strong> is a static report that includes all assessment tasks and the corresponding scores – this report is similar to the Status Report; however, it includes all tasks within WebABLLS.<p>

                <p class="ml-4">To generate a <strong>Baseline Report</strong> click the Baseline Report link from the Actions panel while on the Total Grid View screen.<p>
                
                <p class="ml-4"><strong>Completed Items and Incomplete Items Reports</strong><p>
                <p class="ml-4">The Completed and Incomplete Items Reports are documents that display opposite data – Complete or Incomplete tasks within the WebABLLS assessment. Either report can be generated to include ALL categories or a specific CURRENT category, which can be selected from the Actions panel.<p>

                <p class="ml-4">The <strong>Completed Items Report</strong> generates a simple document that shows a comprehensive list of either ALL tasks, or tasks from a selected category, with scores indicating the student has met the criteria requirements to consider the task complete. To retain this document you must use the Download to PDF link.<p>

                <p class="ml-4">The <strong>Incomplete Items Report</strong> is opposite (from the Completed Items Report) in that it will generate a similar document that reflects a list of either ALL tasks, or tasks from a selected category, with scores indicating the student has not met all of the criteria requirements. To retain this document you must use the Download to PDF link.<p>

                <p class="ml-4">To generate either report click the corresponding link from the action panel while on the Total Grid View screen.  Important: Both documents can be saved to a PDF, but neither document will save to Reports or the Student Summary; <strong>use the Download PDF with ALL Items or Download PDF with CURRENT Category</strong> (displays when a specific category is selected).<p>
               
