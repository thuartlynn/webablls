            <hr class="hr">
            <div>
                <p class="ml-4">WebABLLS will automatically create a date stamp associated with the actual creation date of any WebABLLS generated assessment or report document. </p>

                <p class="ml-4">Most of the time, this date will not correspond with the actual date of any given assessment, worksheet, or report. </p>

                <p class="ml-4">To enter a specific date to display on the printed document, enter a user-defined date into the field labeled: Assigned Date or Assessment Date</p>

                <ul>
                    <li><strong>Create date</strong> = the automatic date stamp the system marks when an assessment or report is created (this date cannot be changed)</li>
                    <li><strong>Assigned date</strong> = the date entered by the user; however, if no date is entered by the user, the default date will be the create date (this date value can be changed)</li>
                    <li><strong>Assessment date</strong> = the label of the date field on the Add Assessment screen; once the assessment is created the Assessment Date is then called the Assigned date <strong>(Assessment date = Assigned date)</strong></li>
                </ul>

                <p class="ml-4">To change the assigned date on an assessment, click the <strong>Details</strong> link for the corresponding assessment</p>

                <p class="ml-4">To change the assigned date on a report, locate the report and update the assigned date field</p>
