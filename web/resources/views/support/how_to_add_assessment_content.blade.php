            <div>
                <h5>Admin + Standard user instructions</h4>

                <p class="ml-4">Locate the <strong>Student Summary</strong>  from the Dashboard or Student List<p>
                <ul>
                    <li><strong>Dashboard</strong> – locate student and click the Student’s Name from the Recent History panel</li>
                    <li><strong>Student List</strong> – click Students tab from top banner, Locate the student and click the Student’s Name from the student list </li>
                </ul>

                <p class="ml-4">The Student Summary screen will display<p>

                <p class="ml-4">Click the <strong>Add Assessment</strong> link from the Student action panel<p>

                <p class="ml-4">The <strong>Add Assessment</strong> screen will display (following the initial display, this screen name is labeled Assessment Details)<p>

                <p class="ml-4"><strong>Complete the assessment details:</strong><p>

                <ul>
                    <li><strong>Assessment Date</strong> | Default is the creation date; to enter a specific Assigned date that corresponds with the actual assessment period</li>
                    <li><strong>Grade Level During the Time of Assessment</strong> | Optional, select from drop-down list</li>
                    <li><strong>Complete Zero Scoring</strong> | Optional, this setting applies ONLY to the initial assessment</li>
                    <li><strong>Is sign language a potential response form?</strong> | Default corresponds with the Student Profile</li>
                    <li><strong>Color</strong> | Required field, select a color from any of the 3 color pallets</li>
                    <li><strong>Program Level During the Time of Assessment</strong> | Optional, but recommended</li>
                </ul>

                <p class="ml-4">Edit Method | Auto-selected based on User Preferences<p>

                <p class="ml-4">Click Continue to proceed to the selected ABLLS-R® assessment edit screen<p>

                <p class="ml-4"><strong>Notes about Assessments:</strong><p>
                <p class="ml-4">The ABLLS-R® assessment is designed to identify both skills a learner has and does not have during a particular assessment period … capturing this data for all 544 skills in the assessment is done over a period of time – depending on the learner, this could be a few days or perhaps even a week or two.<p>

                <p class="ml-4">Each ‘assessment’ in WebABLLS is based on a particular “assessment period” which is coded with a specific score color and dated based on your organization’s guidelines. For example, the assigned assessment date can be based the date the assessment is started or when it is completed. It is important for users to know that within a particular assessment period, only one assessment should be created – one color only.<p>

                <p class="ml-4">Once the assessment is created for a particular period, the user will continue to add all scoring to the current assessment. The Add Assessment link should be used only once during any assessment period.<p>
