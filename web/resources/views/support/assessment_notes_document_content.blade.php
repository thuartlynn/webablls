                <h4>Assessment Notes Document</h4>

                <p class="ml-4"><strong>Assessment Notes</strong></p>
                <p class="ml-4">Assessment Notes correspond with the notes entered while scoring an assessment in any of the three assessment edit modes – Grid, Text, or Cat.</p>
                
                <p class="ml-4">Select options from the <strong>Type</strong> and <strong>Category</strong> panels to generate a listing of task notes entered within the student’s assessment(s). Selection filters include <strong>Show All</strong>, <strong>Show only Open</strong>, or <strong>Show only Private</strong>. Notes can be selected for all categories or a specific category.</p>
                
                <p class="ml-4">ALL assessment notes tagged as either Open or Private can be created, viewed, edited, or deleted by any user with permissions to Edit Assessments and Reports related to a particular student profile.</p>
                <p class="ml-4">To create an <strong>Assessment Notes</strong> document, click Assessment Notes from any corresponding Student Panel.</p>


                <p class="ml-4">Assessment Notes can be tagged as <strong>Open</strong> or <strong>Private</strong> to designate whether a note should be included or excluded when generating an Assessment Notes document that corresponds with the student’s assessment scores.</p>
                <p class="ml-4"><strong>Options include:</strong></p>
                <ul>
                    <li>All notes</li>
                    <li>Only open notes</li>
                    <li>Only private notes</li>
                    <li>Include All Categories (A – Z)</li>
                    <li>Select a specific category (a single category from the assessment)</li>
                </ul>

                <p class="ml-4"><strong>To create an Assessment Notes’ document:</strong></p>
                <p class="ml-4">1. Select the notes’ options (open, private, or all) – use links within the Actions panel</p>
                <p class="ml-4">2. Select ALL categories or a specific category to designate the notes to include – use links within the Category panel</p>
                <p class="ml-4">3. Preview notes displayed within WebABLLS (note, the screen view will display the tag label; however, the tag label, Open or Private, will be omitted on the PDF version)</p>
                <p class="ml-4">4. Click Print to PDF</p>

                <p class="ml-4">The default setting for all Assessment Notes migrated from the WebABLLS Legacy site is <strong>Private</strong>.</p>

                <p class="ml-4">The Notes document will not be retained in WebABLLS; however, after it is downloaded it can be uploaded to the student Files</p>

                <p class="ml-4">1.From the <strong>Assessment Notes</strong> screen, locate and click Files from the Student panel</p>
                <p class="ml-4">2.Locate and click the <strong>Select Files</strong> link from the Actions panel</p>
                <p class="ml-4">3.Locate and click the target file to upload (from your computer)</p>
                <p class="ml-4">4.The upload will begin automatically</p>
                <p class="ml-4">5.When complete, the file will display in the <strong>Student Files</strong> list</p>
                <p class="ml-4">6.To set a <strong>Description</strong>, click the <strong>corresponding row to select the target file</strong> (the row will be highlighted in yellow)</p>
                <p class="ml-4">7.A <strong>Set Description</strong> field will display</p>
                <p class="ml-4">8.<strong>Enter the description</strong> and click <strong>OK</strong></p>
