                <h5>Managing Assessments</h5>
                <p class="ml-4">Saving an Assessment<p>
                <ul>
                    <li>Use the yellow <strong>Save Banner</strong> that automatically appears when a score is entered (there is no need to save after every entry but do save periodically)</li>
                    <li>Use the <strong>Save</strong> link located in the action panel Use the <strong>Save & Continue to next category</strong> link located in the action panel while in Text or Cat edit modes</li>
                </ul>

                <p class="ml-4">Editing an Assessment<p>
                <p class="ml-4">Yes, users can resume scoring or editing an assessment - there are many ways to return to an assessment; however, the simplest way is from the Student Summary<p>
                <ul>
                    <li>Locate your student on the Dashboard, click the student name to get to the Student Summary</li>
                    <li>Locate the target assessment in the Assessments & Reports section within the body of the summary screen</li>
                    <li>Click the hyperlinked <strong>Grid</strong>, <strong>Text</strong>, or <strong>Cat</strong> link to return to the corresponding assessment to continue or resume scoring or to make revisions or updates</li>
                </ul>

                <p class="ml-4">Revising Assessment Details<p>
                <p class="ml-4">Yes, the Assessment Details can be revised<p>
                <ul>
                    <li>Again, locate your student on the Dashboard, click the student name to get to the <strong>Student Summary</strong></li>
                    <li>Locate the target assessment in the Assessments & Reports section within the body of the summary screen</li>
                    <li>Click the hyperlinked word, <strong>Details</strong>, to return to the Assessment Details screen</li>
                    <li>Make revisions or edits to the Assessment Date, the Grade Level, change the assessment color, or add/edit Program Level information</li>
                    <li>Be sure to Save any revisions!​</li>
                </ul>


                <p class="ml-4">Deleting an Assessment<p>
                <p class="ml-4">Yes, the most recent assessment can be deleted<p>
                <ul>
                    <li>Yes again, locate your student on the Dashboard, click the student name to get to the Student Summary</li>
                    <li>Locate the target assessment in the Assessments & Reports section within the body of the summary screen (it is recommended a scored assessment be printed to PDF prior to removal)</li>
                    <li>Click the hyperlinked word, <strong>Details</strong>, to return to the Assessment Details screen</li>
                    <li>Click the Delete Assessment link located in the lower action panel (if the link is not visible, the assessment is either not the most recent assessment or your user account does not have the permissions to remove an assessment)</li>
                    <li>WebABLLS will display a confirmation dialog box, select OK to continue​</li>
                    <li>NOTE: The deletion of an an assessment is permanent; it cannot be retrieved​</li>
                </ul>

                <p class="ml-4">Printing<p>
                <p class="ml-4">There are two components to an assessment in WebABLLS, the Total Grid View (Skills Tracking Grid) that displays the assessment scoring and the content of the assessment that is captured in the Baseline, Completed Items, and Incomplete Items reports; additionally, there is an Assessment Notes document<p>
                <p class="ml-4">All of these reports are available from the <strong>Total Grid View</strong> screen<p>
                <p class="ml-4">From the Student Summary screen, click the <strong>Total Grid View</strong> link from the action panel<p>
                <p class="ml-4">​Reference the Total Grid View action panel and click the link that corresponds with the document to generate<p>
                <p class="ml-4">​Refer to the chart below for a list of the documents/reports related to the  Assessment scores only (there are additional reports that can be generated -- refer to the Reports section for more information on these documents as well as the customized Program Worksheet and corresponding Progress Report as well as the Status Report) <p>

                <img class="ml-4" src="{{ URL::asset('images/support/managing-assessments.jpg') }}">

                <p class="ml-4">Printing the Grid<p>
                <p class="ml-4">From the Total Grid View screen select the Print to PDF or Print to PDF with Age link in the Actions panel of the Total Grid View screen.  The Print to PDF with Age selection will insert the student’s age in months into the checkbox that corresponds with the highest score for a particular assessment.<p>

                <p class="ml-4">Baseline Report<p>
                <p class="ml-4">A Baseline Report is a static report that includes all assessment tasks and the corresponding scores – this report is similar to the Status Report; however, it includes all tasks within WebABLLS.<p>

                <p class="ml-4">Completed Items and Incomplete Items Reports<p>
                <p class="ml-4">The Completed and Incomplete Items Reports are documents that display opposite data – Complete or Incomplete tasks within the WebABLLS assessment. Either report can be generated to include ALL categories or a specific CURRENT category, which can be selected from the Actions panel.
The Completed Items Report generates a simple document that shows a comprehensive list of either ALL tasks, or tasks from a selected category, with scores indicating the student has met the criteria requirements to consider the task complete. To retain this document you must use the Download to PDF link.<p>

                <p class="ml-4">The Incomplete Items Report is opposite (from the Completed Items Report) in that it will generate a similar document that reflects a list of either ALL tasks, or tasks from a selected category, with scores indicating the student has not met all of the criteria requirements. To retain this document you must use the Download to PDF link.<p>

                <p class="ml-4">To generate either report click the corresponding link from the Actions panel while on the Total Grid View screen.  Important: Both documents can be saved to a PDF, but neither document will save to Reports or the Student Summary; use the Download PDF with ALL Items or Download PDF with CURRENT Category (displays when a specific category is selected).<p>

                <p class="ml-4">Assessment Notes<p>
                <p class="ml-4">To create a print document of Assessment Notes, click the Notes link from the Student Panel section of any of the three assessment modes. From the Assessment Notes screen, select the Type (Show All, Show only Open, or Show only Private) and Category (All Categories or select an individual category) to identify the notes to be included in the document, then click the Print to PDF link from the Actions panel. The Notes link is available from the Student Summary screen as well.<p>


