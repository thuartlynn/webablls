                <h5>Scoring an Assessment</h5>
                <p class="ml-4">WebABLLS includes 544 skills (or tasks) within 25 categories (or sections) from the ABLLS-R. The 25 categories are A-Z (excluding O). The action panel functions will vary depending on the screen displayed – links will include Save, Cancel Changes, Grid Mode, Cat Mode, Text Mode, and Assessment Details.<p>
                <p class="ml-4"><strong>NOTE:</strong>  Assessment edit modes are simply a matter of personal choice. There are no features or functions that are different between the three display types. Text Mode resembles the print version of the ABLLS-R Protocol; Grid Mode resembles the skills tracking grids of the protocol; and Cat Mode (short for category) is another format offered to accommodate scoring from a smaller screen, tablet, or smart phone by displaying individual categories of the assessment in the grid style.<p>

                <p class="ml-4"><strong>Assessment Edit Modes</strong></p>
                <p class="ml-4"><strong>Grid Edit</strong> – Assessment displays across three pages (tasks ascending).</p>
                <img class="ml-4" src="{{ URL::asset('images/support/scoring-an-assessment-1.jpg') }}">

                <p class="ml-4"><strong>Text Edit</strong> – Assessment display similar to book format.</p>
                <img class="ml-4" src="{{ URL::asset('images/support/scoring-an-assessment-2.jpg') }}">

                <p class="ml-4"><strong>Cat (Category) Edit</strong> – Assessment displays individual categories (tasks descending).</p>
                <img class="ml-4" src="{{ URL::asset('images/support/scoring-an-assessment-3.jpg') }}">

                <p class="ml-4"><strong>Identifying Tasks</strong></p>
                <p class="ml-4">Note the task, item, or skill within the category along with the corresponding zero-scoring dot and grid section. Each task is assigned a letter identifying the category and a number to identify the particular task within the category. Depending on the scoring criteria for a particular task, the corresponding grid is divided into sections of either 4 boxes or 2 boxes (skills in other categories might have only 1 box).</p>
                <img class="ml-4" src="{{ URL::asset('images/support/scoring-an-assessment-4.jpg') }}">

                <p class="ml-4"><strong>Entering Task Scores</strong></p>
                <p class="ml-4">To enter an actual task score simply click in the corresponding box to activate. WebABLLS features an auto-fill function; click the corresponding box—To score 2, use the second box and WebABLLS will complete boxes 1 and 2; to score 3, use the third box; to score 4, use the fourth box.</p>
                <img class="ml-4" src="{{ URL::asset('images/support/scoring-an-assessment-5.jpg') }}">

                <p class="ml-4">These are five examples of scoring for D1. The first example displays a score of zero with the circle filled. The second example displays a score of one because the student met the criteria. The third example displays a score of two; the fourth example, a score of three; and the fifth example, a score of four.</p>
                <p class="ml-4">Please note the filled-in circle is used with the initial assessment only. If the automatic feature of complete-zero scoring was selected at the beginning of the assessment, the zero scoring will automatically be cleared from any tasks that meet any level of the criteria as scores are entered during the initial assessment.</p>

