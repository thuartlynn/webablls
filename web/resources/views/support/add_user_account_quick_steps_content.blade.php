                    <li>Complete the first name, last name, and email address fields</li>
                    <li>Select the user account role type, and <strong>preferred language</strong></li>
                    <li>Click <strong>Add User</strong> to complete the activation <u>or</u> <strong>Add User + Link Share Permissions</strong> to activate user and link share permissions to designated students</li>
