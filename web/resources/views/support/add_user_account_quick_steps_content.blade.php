                <p class="ml-4">WebABLLS will generate a temporary password that will be sent automatically to the email address registered.<p>

                <p style="color: #B22222;" class="ml-4"><strong>Notes:</strong><p>

                <ul>
                    <li><span style="color: #B22222;">User accounts are not automatically linked with share permissions to student profiles within an organization.</span></li>
                    <li><span style="color: #B22222;">Share permissions can be authorized, revised, or removed at any time.</span></li>
                    <li><span style="color: #B22222;">Error message will occur if an existing user account is already assigned to the email address.</span></li>
