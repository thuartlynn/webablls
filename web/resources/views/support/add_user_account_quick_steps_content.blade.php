                <p class="ml-4">Click the <strong>Add User</strong> link from the action panel<p>
                <p class="ml-4">Complete the form to create a new user account within your organization. All fields must be set. Email must be valid. The user will receive password via welcome email.<p>
                <p class="ml-4">Only one account per email is allowed.<p>

                <ul>
                    <li><strong>Standard user</strong> can use all basic features</li>
                    <li><strong>Organization Administrator user</strong> can use additional organization management features</li>
                    <li><strong>Restricted user</strong> must be authorized with student share permissions to access features</li>
