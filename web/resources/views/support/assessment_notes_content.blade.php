                <p class="ml-4"><strong>Assessment Notes</strong><p>
                <p class="ml-4">Assessment Notes correspond with the notes entered while scoring an assessment in any of the three assessment edit modes – Grid, Text, or Cat.<p>

                <p class="ml-4">Select options from the <strong>Type</strong> and <strong>Category</strong> panels to generate a listing of task notes entered within the student’s assessment(s). Selection filters include <strong>Show All</strong>, <strong>Show only Open</strong>, or <strong>Show only Private</strong>. Notes can be selected for all categories or a specific category.<p>

                <p class="ml-4">ALL assessment notes tagged as either <strong>Open</strong> or <strong>Private</strong> can be created, viewed, edited, or deleted by any user with permissions to Edit Assessments and Reports related to a particular student profile.<p>

                <p class="ml-4">To create an Assessment Notes document, click <strong>Assessment Notes</strong> from any corresponding student action panel.<p>

                <p class="ml-4">Assessment Notes can be tagged as <strong>Open</strong> or <strong>Private</strong> to designate whether a note should be included or excluded when generating an Assessment Notes document that corresponds with the student’s assessment scores.<p>

                <p class="ml-4"><strong>Options include:</strong><p>
                <ul>
                    <li>All notes</li>
                    <li>Only open notes</li>
                    <li>Only private notes</li>
                    <li>Include All Categories (A – Z)</li>
                    <li>Select a specific category (a single category from the assessment)</li>
                </ul>

                <p class="ml-4"><strong>To create an Assessment Notes’ document:</strong><p>
                <ol>
                    <li>Select the notes’ options (open, private, or all) – use links within the Actions panel</li>
                    <li>Select ALL categories or a specific category to designate the notes to include – use links within the Category panel</li>
                    <li>Preview notes displayed within WebABLLS (note, the screen view will display the tag label; however, the tag label, Open or Private, will be omitted on the PDF version)</li>
                    <li>Click Print to PDF</li>
                </ol>

                <p class="ml-4">The Assessment Notes’ document will not appear in the student’s Reports’ list; however, the downloaded PDF can be uploaded to the student’s Files’ portal within WebABLLS.<p>

                <p class="ml-4">The default setting for all Assessment Notes migrated from the WebABLLS Legacy site is <strong>Private</strong>.<p>
