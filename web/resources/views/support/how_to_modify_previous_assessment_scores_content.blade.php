                <h4>How to Modify Previous Assessment Scores</h4>
                <p class="ml-4"><small>Last Updated: Feb 08, 2017 06:33PM PST</small></p>
            </div>
            <hr class="hr">
            <div>

                <p class="ml-4"><strong>Instructions for all Users (with permissions to Edit Assessments & Reports)</strong><p>
                <p class="ml-4"><strong>Quick list of steps</strong><p>

                <ol>
                    <li>Print current Total Grid View</li>
                    <li>Locate and open target assessment to modify</li>
                    <li>Modify score(s)</li>
                    <li>Add Task Note (recommended)</li>
                    <li>Compare to current Total Grid View</li>
                    <li>Save changes</li>
                </ol>

                <hr class="hr">

                <ul>
                    <li>Locate the <strong>Student Summary</strong> from the <u>Dashboard</u> or <u>Student List</u></li>
                    <ul>
                        <li><strong>Dashboard</strong> – locate student and click the <strong>Student’s Name</strong> from the Recent History panel</li>
                        <li>Student List – click Students tab from top banner, locate the student and click the <strong>Student’s Name</strong> from the student list</li>
                    </ul>
                    <li>The <strong>Student Summary</strong> screen will display</li>
                    <li>Click <strong>Total Grid View</strong> from the Student action panel</li>
                    <li>The Total Grid View screen will appear, click <strong>Print to PDF</strong> from the action panel (this step is suggested so you have access to the current scores for reference)</li>
                    <li>Return to the Student Summary screen – click Summary from the Student action panel</li>
                    <li>Locate the <strong>Assessment and Reports</strong> sections of the Student Summary screen</li>
                    <li>Locate the target assessment for modification and click the preferred edit mode (Grid, Text, or Cat) – Grid edit is recommended</li>
                    <li>The corresponding Assessment screen will display  </li>
                    <ul>
                        <li>A yellow warning banner will display across the top of the screen alerting the user this is not the most recent assessment</li>
                        <li>When working with a previous assessment, the scores displayed include only tasks that were scored for the current and previous assessments</li>
                        <li>No scores will display from assessments created following the assessment grid that is open</li>
                        <li>Use the Total Grid View display for reference, if necessary</li>
                    </ul>
                    <li>Locate the score to be modified and make the necessary revisions</li>
                    <li>Click the alphanumeric task code and enter a <strong>Task Note</strong> to explain the revision (recommended, but not necessary if just merging two assessments)</li>
                    <li>When revisions are complete, compare Grid edit screen to the Total Grid View display to insure no future scores were compromised and make adjustments if necessary</li>
                    <li>Save your changes</li>
                </ul>

                <p class="ml-4"><strong>Assessment Notes Document</strong><p>

                <p class="ml-4">To generate an Assessment Notes document to include with the updated Total Grid View document<p>

                <ul>
                    <li>Click Assessment Notes from the Student action panel</li>
                    <li>The Assessment Notes screen will display</li>
                    <li>Use the links within the ‘Type’ action panel to select the note type to display</li>
                    <li>Use the links within the ‘Category’ action panel to select the assessment category of notes to display</li>
                    <li>Use the Print to PDF link to generate a printout of the Assessment Notes document</li>
                </ul>

                <p class="ml-4"><strong>Additional notes</strong> | When any assessment edit screen is open, the Student Assessments action panel will display the legend detail for all associated assessments related to the student. The legend detail for the current display will be greyed out, while the legend detail for other assessments will display the assessment date as a hyperlink allowing the user to click the date to open an alternative assessment.<p>
