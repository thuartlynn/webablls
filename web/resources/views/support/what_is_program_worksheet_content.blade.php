                <p class="ml-4"><I>Note about creation date stamp versus Assigned Date</I><p>

                <p class="ml-4">WebABLLS will automatically create a date stamp associated with the actual creation date of any WebABLLS generated document or assessment. Most of the time, this date will not correspond with the actual date of any given assessment, worksheet, or report. To enter a specific date to display on the printed document, enter a user-defined date into the field labeled: Assigned Date<p>

                <p class="ml-4">The components of the Program Worksheet include five columns—Skills, Objective, Criteria, Current Level, and Program Notes. The following three columns, Objective, Current Level, and Program Notes will display text boxes for modifying and/or entering text. To enter text, simply click in the corresponding text box and type in your information. It is recommended you save your data periodically during this process. Should you need to add or remove any tasks included in the worksheet, use the Reselect Items link from the Actions panel to return to the Total Grid View screen to revise your selections (click Save Changes to return to the worksheet).<p>

                <p class="ml-4">To return to the Program Worksheet to modify entries or continue, the document will display on the Student Summary screen (as well as in the Reports section). Click the Edit link next to the corresponding report displayed on the Student Summary screen or locate the worksheet in the Reports display (using the filters will be helpful), highlight the student row and click the Edit Report link located in the Actions panel.<p>

                <p class="ml-4">Program Worksheets can be downloaded as a PDF or CSV file – click the preferred download from the Actions panel from the Program Worksheet screen or from the Reports screen.<p>
