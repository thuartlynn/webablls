                <p class="ml-4"><strong><I>Administrator user:</I></strong> Each WebABLLS organization (customer account) must have at least one (1) administrator user (note, there can be additional administrator users).  Administrator users should be active users of WebABLLS. An organization administrator user account provides the highest level of access privileges available within WebABLLS. The user at this level is responsible for all the functions and features of account administration and maintenance such as adding user accounts, managing and monitoring account activity, in addition to creating order quotes and placing orders.<p>

                <p class="ml-4"><strong><I>Standard user:</I></strong> A user account with a standard user role should be considered primarily for in-house personnel who will be using WebABLLS on a regular basis. The standard user role has access privileges to create or be assigned a student profile and/or can be authorized with any level of share permissions related any student profile.<p>

                <p class="ml-4"><strong><I>Restricted user:</I></strong> A user account with a restricted user role should be considered for external personnel or other individuals who will not be authorized to create a student profile but can be authorized with various levels of share permissions related to any student profile. [However, this user role cannot be assigned with Owner, Profile Coordinator, or full access with share permissions.] The only way this level account user can use WebABLLS is when share permissions have been authorized by the assigned owner or profile coordinator of a student profile.<p>
