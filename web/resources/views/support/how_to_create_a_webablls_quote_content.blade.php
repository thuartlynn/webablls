                <h4>How to Create a WebABLLS Quote</h4>
                <p class="ml-4"><small>Last Updated: Apr 27, 2017 09:01AM PDT</small></p>
            </div>
            <hr class="hr">
            <div>
                <p class="ml-4"><strong>How to Create a WebABLLS Quote</strong><p>

                <p class="ml-4">Admin user instructions<p>

                <p class="ml-4">Creating an order quote in WebABLLS basically includes the first two steps of creating an order.<p>

                <p class="ml-4">Get started by clicking the <u>Open Order</u> link from the Dashboard or from the Organization tab<p>

                <p class="ml-4"><strong>KEY STEPS</strong><p>
                <p class="ml-4">Open Order link<p>
                <p class="ml-4">Create Extension Order<p>
                <p class="ml-4">Enter order parameters<p>
                <p class="ml-4">Number of years or <span style="color: #B22222;"><strong>Add seats to existing subscription</strong></span><p>
                <p class="ml-4">Extended Seats (select seats to renew)<p>
                <p class="ml-4">Additional Seats<p>
                <p class="ml-4">When to activate (now or later)p>
                <p class="ml-4">Print Preview Summary (PDF) to <u>generate quote</u><p>

                <p class="ml-4"><strong>TIPS</strong><p>
                <p class="ml-4">Use links to toggle back and forth through the order portal<p>
                <p class="ml-4">Use the Cancel link to start over<p>
                <p class="ml-4">Standard pricing for an additional seat is $100 = $40 activation + $60 for a full 12-month extension<p>
                <p class="ml-4">Price calculations for additional seats will be pro-rated @ $5 per month to the Organization’s expiration date<p>
                <p class="ml-4">Additional seats will display the standard activation fee of $40 + the extension per month<p>

                <p class="ml-4"><strong>Step 1 | Create an Order Quote</strong><p>
                <p class="ml-4">Click Open Order<p>
                <p class="ml-4">Click Select Extension Options<p>

                <p class="ml-4">Create Extension Order screen will display<p>
                <p class="ml-4">Select order options:<p>
                <p class="ml-4">Subscription Extension – select number of years for renewal or <span style="color: #B22222;"><strong>select Add seats to existing subscription</strong></span> for additional seats only<p>
                <p class="ml-4">Extended Seats – the 3 choices within the section relate to renewal extensions<p>
                <p class="ml-4">Additional Seats – enter the number of additional seats<p>
                <p class="ml-4">Activate Now – activates seats upon completion of the order; cost is pro-rated to the annual expiration date<p>
                <p class="ml-4">Delayed activation – seats are purchased but are not available until the annual activation date<p>

                <p class="ml-4">Click Confirm<p>

                <p class="ml-4"><strong>Step 2 | Review and confirm order</strong><p>
                <p class="ml-4">The Order Summary screen will display<p>

                <p class="ml-4">Review and verify the Order Summary for accuracy<p>
                <p class="ml-4">Verify the total amount<p>
                <p class="ml-4">Verify the start date<p>
                <p class="ml-4">Verify the expiration date<p>
                <p class="ml-4">Verify the total seat count for your organization<p>

                <p class="ml-4">Need to make a change? Click Select Extension Options to return to the previous screen<p>
                <p class="ml-4">When your order quote is correct, click the Preview Summary link to create a PDF of the preview of your order – this document serves as your quote.<p>

                <p class="ml-4">Note: Users can create different variations of an order quote and use the Preview Summary PDFs to show comparisons between, or amongst, the different options selected.<p>

                <p class="ml-4"><strong>When quote is accepted … Return to the Order Summary screen to proceed with the final steps to complete order.</strong><p>

                <p class="ml-4">Click Confirm Order<p>
                <p class="ml-4">The Billing Address screen will display<p>

                <p class="ml-4"><strong>Step 3 | Billing Address & Checkout</strong><p>
                <p class="ml-4">Verify billing address<p>
                <p class="ml-4">Follow steps to enter Billing Address and click Add New Address<p>
                <p class="ml-4">Or locate and click the Use this Address link if the correct address is available<p>

                <p class="ml-4"><strong>CREDIT CARD Purchases</strong><p>

                <p class="ml-4"><strong>Step 4 | Process Order (checkout)</strong><p>
                <p class="ml-4">The Process order screen will display<p>
                <p class="ml-4">For credit card orders<p>
                <p class="ml-4">Enter payment details<p>
                <p class="ml-4">Click Confirm Payment<p>
                <p class="ml-4">The screen will refresh and display a thank you message – and your order will be activated upon payment confirmation.<p>

                <p class="ml-4"><strong>Step 5 | Print order documents</strong><p>
                <p class="ml-4">Click the Orders link from the action panel – the last link in the list<p>
                <p class="ml-4">Your order will appear in the table<p>
                <p class="ml-4">Click on the row that corresponds with the order (it will highlight in yellow)<p>
                <p class="ml-4">Click the Download Summary and Download Invoice links to generate a PDF of the files<p>
                <p class="ml-4">Note | It may take a few minutes for the Invoice file to be generated and appear in WebABLLS<p>

                <p class="ml-4"><strong>PURCHASE ORDERS (or other forms of payment)</strong><p>

                <p class="ml-4"><strong>Step 4 | Process Order (checkout)</strong><p>
                <p class="ml-4">The Process order screen will display<p>
                <p class="ml-4">Scroll down to the Other Payment Methods section<p>
                <p class="ml-4">Read the instructions<p>
                <p class="ml-4">Click Confirm Order without Credit Card<p>
                <p class="ml-4">The screen will refresh and display a thank you message – and your order details are stored within your WebABLLS organization<p>

                <p class="ml-4"><strong>Step 5 | Print order documents</strong><p>
                <p class="ml-4">Click the Orders link from the action panel – the last link in the list<p>
                <p class="ml-4">Your order will appear with a status of Payment pending/Waiting for Confirmation<p>
                <p class="ml-4">Click on the row that corresponds with the order (it will highlight in yellow)<p>
                <p class="ml-4">Click the Download Summary and Download Invoice links to generate a PDF of the files<p>
                <p class="ml-4">Note | It may take a few minutes for the Invoice file to be generated and appear in WebABLLS<p>

                <p class="ml-4"><strong>Step 6 | Submit Order documents with order</strong><p>
                <p style="color: #B22222;" class="ml-4"><strong>IMPORTANT! DO NOT SKIP THIS STEP</strong><p>
                <p class="ml-4">Print both the Order Summary and the preliminary invoice and submit to the Order Department with your purchase order or other payment method<p>
                <p class="ml-4">Upon receipt and verification of order documents and purchase order or payment by the Order Department,  the WebABLLS Support Team will confirm the activation of the order<p>
