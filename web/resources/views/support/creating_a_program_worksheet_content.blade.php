                <h4>Creating a Program Worksheet</h4>
                <p class="ml-4"><small>Last Updated: Oct 10, 2016 12:36PM PDT</small></p>
            </div>
            <hr class="hr">
            <div>
                <p class="ml-4"><strong>To create a Program Worksheet</strong> (formerly called an IEP Worksheet in WebABLLS Legacy)<p>
                <p class="ml-4">From the <strong>Dashboard</strong> screen:<p>

                <ol>
                    <li>Click the student’s <strong>Name</strong> from the <strong>Recent History</strong> section</li>
                    <li>The <strong>Student Summary</strong> screen will display</li>
                    <li>Locate and click on the <strong>Total Grid View</strong> link from the Actions panel</li>
                    <li>The Total Grid View screen will display</li>
                    <li><strong>Select (or highlight) the task</strong> by clicking on the tasks to include in the Program Worksheet</li>
                    <li>After you have selected the tasks to include, click the <strong>Generate Program Worksheet</strong> link from the Actions panel</li>
                    <li>The Program Worksheet will display</li>
                    <li>Using the drop-down menu, select a <strong>Title</strong> for your report (the default setting is IEP Worksheet)</li>
                    <li>Enter the <strong>Assigned date</strong> for your report (the default setting is the current date)</li>
                    <li>Review your selected tasks, make revisions to the open fields – Objective, Current Level, Notes</li>
                    <li>It is recommend you enter information into the Current Level field (the information is useful when you generate a Progress Report based on the Program Worksheet because the text entered as Current Level will automatically populate the Previous Level fields)</li>
                    <li>Add any notes (the report can be printed with or without notes for your convenience)</li>
                    <li>Locate and click the <strong>Save Changes</strong> link from the yellow Save banner or the Actions panel</li>
                    <li>The Program Worksheet is added to your Report list upon creation and can be retrieved from the Student Summary screen or your Reports list</li>
                </ol>

                <p class="ml-4"><strong>Reselect Items to include in a Program Worksheet</strong><p>
                <p class="ml-4">From the <strong>Dashboard</strong> screen:<p>
                <ol>
                    <li>Click the student’s <strong>Name</strong> from the <strong>Recent History</strong> section</li>
                    <li>The Student Summary screen will display</li>
                    <li>Locate the target Program Worksheet in the <strong>Assessments and Reports</strong> section</li>
                    <li>Click the <strong>Edit</strong> link next to the corresponding Program Worksheet</li>
                    <li>The selected Program Worksheet will display</li>
                    <li>Locate and click the <strong>Reselect Items link</strong> in the Actions panel</li>
                    <li>The Total Grid View screen will display</li>
                    <li>Select or unselect the desired tasks</li>
                    <li>Locate and click the <strong>Save Changes</strong> link located in the Actions panel</li>
                </ol>
            </div>

