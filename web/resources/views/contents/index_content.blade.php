        <h1>Assess</h1>
        <p class="text-justify">At the core of WebABLLS is the assessment component of the ABLLS-R® developed by Dr. Partington. This assessment can be used by educators, clinicians, and parents to facilitate the identification of skills needed by their learners to communicate effectively and learn from everyday experiences.
        </p>
        <h6>WebABLLS features 3 edit modes for scoring assessments:</h6>
        <li>Text Mode resembles the print version of the ABLLS-R® Protocol</li>
        <li>Grid Mode resembles the skills tracking grids of the Protocol</li>
        <li>Cat Mode (short for category) is another format offered to accommodate scoring from a smaller screen</li>
        <p class="text-justify">The criterion-referenced assessment within WebABLLS provides a comprehensive review of 544 skills from 25 skill areas including language, social interaction, self-help, academic, and motor skills. The task items within each skill area are arranged from simpler to more complex tasks. Expressive language skills are assessed based on the Behavioral Analysis of Language as presented by Dr. B.F. Skinner in his book, Verbal Behavior (1957).</p>
